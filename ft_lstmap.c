#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list *new_node;
	t_list *new_head;

	new_head = NULL;
	while (lst)
	{
		new_node = ft_lstnew(lst->content);
		if (new_node == NULL)
		{
			del(lst->content);
			ft_lstclear(&new_head, del);
			return (NULL);
		}
		ft_lstadd_back(&new_head, new_node);
		lst = lst->next;
	}
	return (new_head);
}