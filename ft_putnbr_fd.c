#include "libft.h"
#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	s;

	if (n < 0)
	{
		write (fd, '-', 1);
		n *= -1;
	}
	s = n + '0';
	write (fd, &s, 1);
}
